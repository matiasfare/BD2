CREATE TABLE PACIENTE
(CEDULA   VARCHAR2(11) NOT NULL CONSTRAINT PK_PACIENTE PRIMARY KEY,
 NOMBRE   VARCHAR2(30) NOT NULL,
 APELLIDO VARCHAR2(30) NOT NULL,
 FECHA_NACIMIENTO DATE NOT NULL,
 DIRECCION        VARCHAR2(60) NOT NULL,
 TELEFONO         VARCHAR2(20) NOT NULL,
 CORREO_ELECTRONICO VARCHAR2(60) NOT NULL,
 ID_SEGURO        NUMBER(8));

CREATE TABLE TRATAMIENTO
(ID_TRATAMIENTO    NUMBER(8) NOT NULL CONSTRAINT PK_TRATAMIENTO PRIMARY KEY,
 DESCRIPCION VARCHAR2(2000) NOT NULL,
 NRO_SESIONES  NUMBER(2) NOT NULL,
 CEDULA        VARCHAR2(11) NOT NULL,
 NRO_TURNO     NUMBER(2) NOT NULL,
 COD_FISIOTERAPEUTA   NUMBER(8) NOT NULL,
 FECHA_INICIO DATE NOT NULL,
 FECHA_FIN  DATE,
 OBSERVACION VARCHAR2(500));

 CREATE TABLE EQUIPAMIENTO
( ID_APARATO NUMBER(4) NOT NULL CONSTRAINT PK_EQIPO PRIMARY KEY,
  NOMBRE     VARCHAR2(30) NOT NULL,
  CARACTERISTICAS VARCHAR2(200) NOT NULL);

CREATE TABLE FERIADO
 (DIA_FERIADO DATE NOT NULL);

CREATE TABLE EQUIPAMIENTO_UTILIZADO
( ID_TRATAMIENTO NUMBER(8) NOT NULL,
  ID_APARATO NUMBER(4) NOT NULL);

CREATE TABLE SEGURO_MEDICO
(ID_SEGURO    NUMBER(8) NOT NULL CONSTRAINT PK_SEGURO PRIMARY KEY,
 NOMBRE_SEGURO VARCHAR2(60) NOT NULL);

CREATE TABLE HORARIO
(NRO_TURNO NUMBER(2) NOT NULL CONSTRAINT PK_HORARIO PRIMARY KEY,
 HORA_INICIO VARCHAR2(5) NOT NULL);

CREATE TABLE FISIOTERAPEUTA
(COD_FISIOTERAPEUTA NUMBER(8) NOT NULL CONSTRAINT PK_FISIO PRIMARY KEY,
 NOMBRE VARCHAR2(30) NOT NULL,
 APELLIDO VARCHAR2(30) NOT NULL);

ALTER TABLE PACIENTE ADD 
(CONSTRAINT FK_SEGURO_PACIENTE FOREIGN KEY (ID_SEGURO) REFERENCES SEGURO_MEDICO (ID_SEGURO));

ALTER TABLE TRATAMIENTO ADD
(CONSTRAINT FK_PACIENTE_TRATAM FOREIGN KEY(CEDULA) REFERENCES PACIENTE (CEDULA),
 CONSTRAINT FK_HORARIO_TRATAM  FOREIGN KEY(NRO_TURNO) REFERENCES HORARIO(NRO_TURNO),
 CONSTRAINT FK_FISIO_TRATAM   FOREIGN KEY(COD_FISIOTERAPEUTA) REFERENCES 
            FISIOTERAPEUTA(COD_FISIOTERAPEUTA));

ALTER TABLE EQUIPAMIENTO_UTILIZADO ADD
(CONSTRAINT FK_EQUIPO_EQUIPUTILIZ FOREIGN KEY(ID_APARATO) REFERENCES EQUIPAMIENTO(ID_APARATO), 
 CONSTRAINT FK_TRATAM_EQUIPO FOREIGN KEY(ID_TRATAMIENTO) REFERENCES TRATAMIENTO(ID_TRATAMIENTO));
